#include "libft.h"

int	ft_atoi(char const *str)
{
	int	result;
	int	sig;
	int	i;

	i = 0;
	result = 0;
	sig = 1;
	while (ft_isspace(str[i]) == 1)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sig *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (result * sig);
}
